def draw_state(game_state: Game, screen_used: pygame.surface.Surface):
    # Fonction pour rafraichire la fenêtre
    screen_used.fill((0, 0, 0))
    # Je detemine combien de cellule vont etre présente pas ligne et collone
    pixel_row: float = int(width / len(game_state.get_state()))
    pixel_collumn: float = int(length / len(game_state.get_state()[0]))
    for row in range(0, len(game_state.get_state())):

        # Je détermine la place de la cellule par rapport au nombre de cellule
        place_row: int = int(pixel_row * row)
        for col in range(0, len(game_state.get_state()[0])):

            place_col: int = int(pixel_collumn * col)
            if game_state.get_state()[row][col] == 1:

                # Je change la couleur en fonction de l'état de la cellule
                # (si elle est vivante ou non)
                pygame.draw.rect(screen_used, (20, 20, 20), (place_row, place_col, pixel_row - 1, pixel_collumn - 1))
            else:

                pygame.draw.rect(screen_used, (220, 220, 220), (place_row, place_col, pixel_row - 1, pixel_collumn - 1))
    pygame.display.flip()
    game.get_next_state()


screen: pygame.surface.Surface = pygame.display.set_mode((width, length))
clock = pygame.time.Clock()

done: bool = False

# While the game is not over
while not done:

    # Listen for all events
    for event in pygame.event.get():

        # Quit the infinite loop when the user presses the close button
        if event.type == pygame.QUIT:
            done = True

    draw_state(game, screen)
    clock.tick(5)

pygame.quit()
